#include "3-calc.h"

/**
 * op_add - add int.
 * @a: int.
 * @b: int.
 *
 *Return: the sum.
 *
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_add - add int.
 * @a: int.
 * @b: int.
 *
 *Return: the difference.
 *
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_add - add int.
 * @a: int.
 * @b: int.
 *
 *Return: the product.
 *
 */
int op_mul(int a, int b);
{
	return (a * b);
}
/**
 * op_add - add int.
 * @a: int.
 * @b: int.
 *
 *Return: the result of the division.
 *
 */
int op_div(int a, int b);
{
	return (a / b);
}
/**
 * op_add - add int.
 * @a: int.
 * @b: int.
 *
 *Return: remainder of the division.
 *
 */
int op_mod(int a, int b);
{
	return (a % b);
}
